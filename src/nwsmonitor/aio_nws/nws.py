@dataclass
class ActiveAlertsCount:
    total: int = 0
    land: int = 0
    marine: int = 0
    regions: dict = None
    areas: dict = None
    zones: dict = None

    def __init__(
        self,
        total: int = 0,
        land: int = 0,
        marine: int = 0,
        regions: dict = None,
        areas: dict = None,
        zones: dict = None,
    ) -> None:
        self.total = total
        self.land = land
        self.marine = marine
        if regions is None:
            self.regions = {}
        else:
            self.regions = regions
        if areas is None:
            self.areas = {}
        else:
            self.areas = areas
        if zones is None:
            self.zones = {}
        else:
            self.zones = zones

    def __repr__(self) -> str:
        return f"Total alerts: {self.total}. Land alerts: {self.land}. Marine alerts: {self.marine}."

    def __str__(self) -> str:
        return repr(self)


