async def point_forecast(
    point: Tuple[float, float], units: Optional[Literal["us", "si"]] = "us"
) -> Tuple[Any, pd.DataFrame]:
    async with aiohttp.ClientSession(
        base_url=BASE_URL_NWS, raise_for_status=check_status
    ) as session:
        html_point = f"{point[0]},{point[1]}"
        data = await fetch(session, f"/points/{html_point}", NWS_DATA_FORMAT)
        wfo = data["cwa"]
        x = data["gridX"]
        y = data["gridY"]
        gridpoint = f"/gridpoints/{wfo}/{x},{y}"
        forecast = await fetch(
            session, f"{gridpoint}/forecast", NWS_DATA_FORMAT, units=units
        )
        stations = await fetch(session, f"{gridpoint}/stations", NWS_DATA_FORMAT)
        stations = pd.DataFrame(stations["@graph"])
        station = stations["stationIdentifier"][0]
        obs = await fetch(
            session,
            f"/stations/{station}/observations/latest",
            NWS_DATA_FORMAT,
            require_qc="false",
            no_cache=True,
        )
        return obs, pd.DataFrame(forecast["periods"])


async def get_forecast(
    address: str, units: Optional[Literal["us", "si"]] = "us"
) -> Tuple[Any, pd.DataFrame, Location]:
    point, location = locate(address)
    return await point_forecast(point, units) + (location,)


async def ffg(address: str, valid: Optional[datetime.datetime] = None) -> pd.DataFrame:
    params = {}
    point = locate(address)[0]
    params["lon"] = point.lon
    params["lat"] = point.lat
    if valid:
        if valid.tzinfo is None:
            raise ValueError("A time zone must be specified.")
        params["valid"] = valid.isoformat()
    async with aiohttp.ClientSession(
        base_url=BASE_URL_IEM, raise_for_status=True
    ) as session:
        data = await fetch(session, f"{BASE_API_PATH_IEM}/ffg_bypoint.json", **params)
        return pd.DataFrame(data["ffg"])


async def spc_wpc_outlook(
    *,
    day: int = 1,
    type: Union[str, int] = 0,
    cat: Union[str, int] = 0,
    extent: Union[str, int] = 1,
    wfo: str = "LWX",
    fema: int = 3,
    csector: str = "conus",
    valid: Optional[datetime.datetime] = None,
    no_cache: bool = False,
    **kwargs,
):
    """
    Wrapper for IEM Autoplot app #220.
    By default, fetch the current SPC Day 1 Convective Outlook for CONUS.
    """
    type_real = None
    match type:
        case 0 | "conv" | "C" | "Convective":
            type_real = "C"
        case 1 | "fire" | "F" | "Fire Weather":
            type_real = "F"
        case 2 | "ero" | "E" | "Excessive Rainfall":
            type_real = "E"
        case _:
            raise ValueError("Unknown outlook type.")
    which = f"{day}{type_real}"
    cat_real = None
    if type_real == "C":
        match cat:
            case 0 | "categorical":
                cat_real = "categorical"
            case 1 | "tornado":
                cat_real = "tornado"
            case 2 | "hail":
                cat_real = "hail"
            case 3 | "wind":
                cat_real = "wind"
            case _:
                raise ValueError("Invalid outlook category.")
    else:
        cat_real = "categorical"
    t = None
    match extent:
        case 0 | "wfo" | "cwa":
            t = "cwa"
        case 1 | "state" | "sector" | "state/sector":
            t = "state"
        case 2 | "fema" | "fema region":
            t = "fema"
        case _:
            raise ValueError("Invalid extent type.")
    if wfo is None and t == "cwa":
        raise ValueError("You must specify a WFO.")
    if not fema and t == "fema":
        raise ValueError("You must specify a FEMA region number.")
    if csector is None and t == "state":
        raise ValueError("You must specify a state or sector.")
    await fetch_autoplot(
        number=220,
        no_cache=no_cache,
        date=valid,
        which=which,
        cat=cat_real,
        t=t,
        wfo=wfo,
        fema=fema,
        csector=csector,
        **kwargs,
    )
