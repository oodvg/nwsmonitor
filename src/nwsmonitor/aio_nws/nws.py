class AutoplotParser(HTMLParser):
    """
    This class has one purpose: To find the image generated by using
    IEM's autoplot function.
    """

    _img_url = None

    def handle_starttag(self, tag, attrs):
        if tag == "img":
            if ("id", "theimage") in attrs:
                for attr, val in attrs:
                    if attr == "src":
                        self._img_url = val
                        break

    def reset(self):
        super().reset()
        self._img_url = None

    @property
    def img_url(self):
        return self._img_url


async def check_status(response: aiohttp.ClientResponse) -> None:
    if response.status not in {200, 301}:
        details = await response.json()
        headers = response.headers
        raise RuntimeError(f"Status {response.status}. {details=}; {headers=}")


async def fetch(
    client: aiohttp.ClientSession,
    api_call: str,
    accept: Optional[str] = None,
    no_cache: bool = False,
    **kwargs,
) -> Any:
    headers = {"User-Agent": USER_AGENT}
    if accept:
        headers["Accept"] = accept
    if no_cache:
        headers["Cache-Control"] = "no-cache"
    _log.debug(f"Fetching {client._base_url}{api_call} with {headers=} and {kwargs=}")
    async with client.get(
        api_call,
        params=kwargs,
        headers=headers,
        raise_for_status=check_status,
        timeout=aiohttp.ClientTimeout(total=60),
    ) as resp:
        _log.debug(f"Response headers: {resp.headers}")
        try:
            return await resp.json()
        except aiohttp.ClientResponseError:
            return await resp.text()


async def fetch_autoplot(
    number: int = 220,
    no_cache: bool = False,
    date: Optional[datetime.datetime] = None,
    **kwargs,
) -> None:
    headers = {"User-Agent": USER_AGENT}

    # Delete kwargs that are set to None
    to_delete = set()
    for kwarg, val in kwargs.items():
        if val is None:
            to_delete.add(kwarg)
    for kwarg in to_delete:
        del kwargs[kwarg]

    if no_cache:
        kwargs["_cb"] = 1
    kwargs["q"] = number
    if date:
        kwargs["valid"] = date.strftime("%Y/%m/%d+%H%M")

    async with aiohttp.ClientSession(
        base_url=BASE_URL_IEM, raise_for_status=True
    ) as session:
        parser = AutoplotParser()
        async with session.get(
            AUTOPLOT_PATH_IEM,
            params=kwargs,
            headers=headers,
            raise_for_status=check_status,
            timeout=aiohttp.ClientTimeout(total=60),
        ) as resp:
            _log.debug(f"Response headers: {resp.headers}")
            text = await resp.text()
            async with aiofiles.open("debug.html", "w") as fp:
                await fp.write(text)
            parser.reset()
            parser.feed(text)
            if parser.img_url is None:
                raise RuntimeError(
                    "Autoplot request succeeded but the image was not found."
                )

        async with session.get(
            parser.img_url,
            raise_for_status=check_status,
            timeout=aiohttp.ClientTimeout(total=60),
        ) as resp:
            async with aiofiles.open("autoplot.png", "wb") as img:
                await img.write(await resp.read())


def locate(address: str) -> Tuple[Point, Location]:
    geolocator = Nominatim(user_agent=USER_AGENT)
    location = geolocator.geocode(address, country_codes="us", timeout=20)
    if location is None:
        raise RuntimeError(f"Could not geolocate {address} within the US.")
    return Point(location.latitude, location.longitude), location


async def afos(
    cccc: Optional[str] = None,
    pil: Optional[str] = None,
    date: Optional[datetime.date] = None,
) -> pd.DataFrame:
    params = dict()
    if not (cccc or pil):
        raise ValueError("Either cccc or pil must be set.")

    if cccc:
        params["cccc"] = cccc

    if pil:
        params["pil"] = pil

    if date:
        params["date"] = date.isoformat()

    async with aiohttp.ClientSession(
        base_url=BASE_URL_IEM, raise_for_status=True
    ) as session:
        data = await fetch(session, f"{BASE_API_PATH_IEM}/nws/afos/list.json", **params)
        if not isinstance(data, dict):
            raise RuntimeError(f"Expected a dict, got {type(data).__name__}.")
        return pd.DataFrame(data["data"])


